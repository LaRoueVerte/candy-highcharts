package def.highcharts;
/**
 * The font metrics.
 */
@jsweet.lang.Interface
public abstract class FontMetricsObject extends def.js.Object {
    /**
     * The baseline relative to the top of the box.
     */
    public double b;
    /**
     * The font size.
     */
    public double f;
    /**
     * The line height.
     */
    public double h;
}

