package def.highcharts;
/**
 * (Highcharts, Highstock, Highmaps, Gantt) Descriptions of lesser known series
 * types. The relevant description is added to the screen reader information
 * region when these series types are used.
 */
@jsweet.lang.Interface
public abstract class LangAccessibilitySeriesTypeDescriptionsOptions extends def.js.Object {
    @jsweet.lang.Optional
    public String arearange;
    @jsweet.lang.Optional
    public String areasplinerange;
    @jsweet.lang.Optional
    public String boxplot;
    @jsweet.lang.Optional
    public String bubble;
    @jsweet.lang.Optional
    public String columnrange;
    @jsweet.lang.Optional
    public String errorbar;
    @jsweet.lang.Optional
    public String funnel;
    @jsweet.lang.Optional
    public String pyramid;
    @jsweet.lang.Optional
    public String waterfall;
}

