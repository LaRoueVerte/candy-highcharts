package def.highcharts;
/**
 * (Highcharts, Highstock, Highmaps, Gantt) Axis description format strings.
 */
@jsweet.lang.Interface
public abstract class LangAccessibilityAxisOptions extends def.js.Object {
    @jsweet.lang.Optional
    public String rangeCategories;
    @jsweet.lang.Optional
    public String rangeFromTo;
    @jsweet.lang.Optional
    public String timeRangeDays;
    @jsweet.lang.Optional
    public String timeRangeHours;
    @jsweet.lang.Optional
    public String timeRangeMinutes;
    @jsweet.lang.Optional
    public String timeRangeSeconds;
    @jsweet.lang.Optional
    public String xAxisDescriptionPlural;
    @jsweet.lang.Optional
    public String xAxisDescriptionSingular;
    @jsweet.lang.Optional
    public String yAxisDescriptionPlural;
    @jsweet.lang.Optional
    public String yAxisDescriptionSingular;
}

